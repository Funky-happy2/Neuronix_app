export function getTournamentTier(xp: number) {
  let tier = TOURNAMENT_TIERS[0];
  for (const t of TOURNAMENT_TIERS) {
    if (xp >= t.minXp) tier = t;
    else break;
  }
  const tierIndex = TOURNAMENT_TIERS.indexOf(tier);
  const nextTier = TOURNAMENT_TIERS[tierIndex + 1];
  const xpInTier = xp - tier.minXp;
  const xpForNext = nextTier ? nextTier.minXp - tier.minXp : 1;
  const progress = nextTier ? Math.min(xpInTier / xpForNext, 1) : 1;
  return { ...tier, tierIndex, nextTier: nextTier || null, xpInTier, xpForNext, progress };
}

