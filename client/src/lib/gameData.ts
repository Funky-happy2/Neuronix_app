export function getRebirthRequirements(rebirthLevel: number): RebirthRequirement {
  const n = rebirthLevel;
  return {
    level: Math.min(10 + n * 3, 100),
    badges: Math.min(Math.floor(n / 2) + 3, 65),
    gamesPlayed: 25 + n * 10,
    shopItems: Math.min(Math.floor(n / 3) + 2, 55),
    bossesDefeated: n >= 2 ? Math.min(1 + Math.floor((n - 2) / 2), 25) : 0,
    longestStreak: n >= 3 ? Math.min(2 + (n - 3), 30) : 0,
    xp: n >= 4 ? Math.min(5000 + (n - 4) * 3000, 150000) : 0,
    dailyChallenges: n >= 5 ? Math.min(3 + (n - 5) * 2, 60) : 0,
    totalCoins: n >= 7 ? Math.min(2000 + (n - 7) * 2000, 100000) : 0,
    description: `Rebirth ${n + 1}`,
  };
}

export function getRebirthMultiplier(rebirthLevel: number): number {
  return 100 + rebirthLevel * 5;
}

